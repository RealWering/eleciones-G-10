    private void buttonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarActionPerformed

        String numeroCedula = campoCedula.getText();
        String nombre = campoNombre.getText();
        String telefono = campoTelefono.getText();
        String ciudad = campoCiudad.getText();
        String partido = comboPartido.getSelectedItem().toString();
        String descripcion = textDescripcion.getText();
        String mensajeCampania = textMensajeCampania.getText();

        ClsCandidato candidato = new ClsCandidato(partido, ciudad, descripcion, mensajeCampania, "", numeroCedula, nombre, telefono);
        ClsMensaje mensaje= this.controlador.AgregarCandidato(candidato);
        
            
         JOptionPane.showMessageDialog(rootPane,mensaje.getDescripcion()+ "# Cedula "+numeroCedula + " - "+ nombre);   
       
        campoCedula.setText("");
        campoNombre.setText("");
        campoTelefono.setText("");
        campoCiudad.setText("");
        textDescripcion.setText("");
        textMensajeCampania.setText("");      
        
        
        
        
       
    }//GEN-LAST:event_buttonAgregarActionPerformed

