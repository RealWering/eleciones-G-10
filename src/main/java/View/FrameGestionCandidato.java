     public void ObtenerCandidatos() {

        LinkedList<ClsCandidato> candidatos = this.controlador.ObtenerCandidatos();
        this.ActualizarListaCandidatos(candidatos);
    }

    public void ActualizarListaCandidatos(LinkedList<ClsCandidato> lista) {
        DefaultTableModel model = (DefaultTableModel) this.listaCandidatos.getModel();
        model.setRowCount(0);
        for (ClsCandidato candidato : lista) {
            Object[] row = {candidato.getNumeroDoc(), candidato.getNombre(), candidato.getPartidoPolitico(), candidato.getCiudadOrigen()};
            model.addRow(row);
        }
    }

