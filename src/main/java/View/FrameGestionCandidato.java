    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed
        // TODO add your handling code here:
        int column =0;
        int row = this.listaCandidatos.getSelectedRow();
        String numDoc = this.listaCandidatos.getModel().getValueAt(row, column).toString();
        
        String nombre = this.listaCandidatos.getModel().getValueAt(row, column+1).toString();
       ClsMensaje mensaje=this.controlador.EliminarCandidato(numDoc);
       if(mensaje !=null){
       JOptionPane.showMessageDialog(rootPane,mensaje.getDescripcion()+ "# Cedula "+numDoc + " - "+ nombre);
       this.ObtenerCandidatos();
       }
        
       
        
    }//GEN-LAST:event_buttonEliminarActionPerformed

