    public void ObtenerVotantes() {

        LinkedList<ClsVotante> candidatos = this.controlador.ObtenerVotantes();
        this.ActualizarListaVotantes(candidatos);
    }

    public void ActualizarListaVotantes(LinkedList<ClsVotante> lista) {
        DefaultTableModel model = (DefaultTableModel) this.listaVotantes.getModel();
        model.setRowCount(0);
        for (ClsVotante votante : lista) {
            Object[] row = {votante.getNumeroDoc(), votante.getNombre(),votante.getCiudadRegistro()};
            model.addRow(row);
        }
    }

