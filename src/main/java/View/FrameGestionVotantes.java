    private void buttonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarActionPerformed
        // TODO add your handling code here:
        String numeroCedula = campoCedula.getText();
        String nombre = campoNombre.getText();
        String telefono = campoTelefono.getText();
        String ciudad = campoCiudad.getText();
        String correo = campoCorreo.getText();

        ClsVotante votante = new ClsVotante(ciudad, correo, numeroCedula, nombre, telefono);
        ClsMensaje mensaje = this.controlador.AgregarVotante(votante);
        
        JOptionPane.showMessageDialog(rootPane,mensaje.getDescripcion()+ "# Cedula "+numeroCedula + " - "+ nombre);   
       
        campoCedula.setText("");
        campoNombre.setText("");
        campoTelefono.setText("");
        campoCiudad.setText("");
        campoCorreo.setText("");
        
    }//GEN-LAST:event_buttonAgregarActionPerformed

    private void buttonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBorrarActionPerformed
           
        // TODO add your handling code here:
         int column =0;
        int row = this.listaVotantes.getSelectedRow();
        String numDoc = this.listaVotantes.getModel().getValueAt(row, column).toString();
        
        String nombre = this.listaVotantes.getModel().getValueAt(row, column+1).toString();
       ClsMensaje mensaje=this.controlador.EliminarVotante(numDoc);
       if(mensaje !=null){
       JOptionPane.showMessageDialog(rootPane,mensaje.getDescripcion()+ "# Cedula "+numDoc + " - "+ nombre);
       this.ObtenerVotantes();
       }
    }//GEN-LAST:event_buttonBorrarActionPerformed

