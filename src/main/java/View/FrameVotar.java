    private void buttonVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonVotarActionPerformed
        // TODO add your handling code here:
        String cedula = campoCedula.getText();
        String[] y = this.comboEleccionesAbiertas.getSelectedItem().toString().split("-", 2);
        String h = y[0];
        int column = 0;
        int row = this.ListaCandidatos.getSelectedRow();
        String ide = this.ListaCandidatos.getModel().getValueAt(row, column).toString();

        ClsMensaje mensaje = this.controladorVoto.Votar(cedula, h, ide);
        if (mensaje != null) {
            JOptionPane.showMessageDialog(rootPane, mensaje.getDescripcion() + " " + y[1]);
            this.ObtenerInscritos();
        }

    }//GEN-LAST:event_buttonVotarActionPerformed

