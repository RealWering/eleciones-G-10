    public void ObtenerInscritos() {

        String[] y = this.comboEleccionesAbiertas.getSelectedItem().toString().split("-", 2);
        String h = y[0];

        LinkedList<ClsCandidato> inscritos = this.controladorcandidato.ObtenerInscritos(h);

        this.ActualizarListaInscritos(inscritos);

    }

    public void ActualizarListaInscritos(LinkedList<ClsCandidato> lista) {
        DefaultTableModel model = (DefaultTableModel) this.ListaCandidatos.getModel();
        model.setRowCount(0);
        for (ClsCandidato inscrip : lista) {
            Object[] row = {inscrip.getNumeroDoc(), inscrip.getNombre(), inscrip.getPartidoPolitico()};
            model.addRow(row);
        }

    }

    public void ObtenerEleccionesA() {

        LinkedList<ClsElecciones> elecciones = this.controlador.ObtenerEleccionesA();

        DefaultComboBoxModel model = (DefaultComboBoxModel) this.comboEleccionesAbiertas.getModel();
        model.removeAllElements();

        for (ClsElecciones eleccion : elecciones) {
            model.addElement(eleccion.getId() + "-" + eleccion.getNombre() + " " + eleccion.getTipo());

        }

        this.comboEleccionesAbiertas.setModel(model);
    }

